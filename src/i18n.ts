export function t(key: string, lang: Language = "ko", ...args: any[]): string {
  const keys = key.split(".");
  let value: any = translations[lang];

  for (const k of keys) {
    if (value && typeof value === "object" && k in value) {
      value = value[k];
    } else {
      return key; // Fallback to key if translation not found
    }
  }

  // Handle function translations (like stepOf)
  if (typeof value === "function") {
    return value(...args);
  }

  return String(value);
}
